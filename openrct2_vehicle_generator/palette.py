# ---------------------------------------------------------------------------
# sRGB -> linear conversion
# ---------------------------------------------------------------------------


def _srgb2linear(x: np.ndarray) -> np.ndarray:
    x = np.asarray(x, dtype=np.float64)
    out = np.empty_like(x)
    lo = x <= 0.04045
    out[lo] = x[lo] / 12.92
    out[~lo] = np.power((x[~lo] + 0.055) / 1.055, 2.4)
    return out


